    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveActionPerformed
    {//GEN-HEADEREND:event_btnSaveActionPerformed
        if (btnSave.getText().equals("Save"))
        {
            try
            {
                String query ="insert into student(student_id, student_name, batch) VALUES (null, '"+
                txtStudentName.getText() +"' ,'"+
                txtBatch.getText() +"')";
                databaseConnection.s.executeUpdate(query);
                loadStudents();
                clear();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
            try
            {
                String query = "update student set student_name=' "+
                txtStudentName.getText() + "', batch='"+
                txtBatch.getText() +"' where student_id=" + studentID;
                databaseConnection.s.executeUpdate(query);
                btnSave.setText("Save");
                clear();
                loadStudents();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDeleteActionPerformed
    {//GEN-HEADEREND:event_btnDeleteActionPerformed
        try
        {
            String query = "delete from student where student_id='"+ studentID +"'";
            int input = JOptionPane.showConfirmDialog(this, "Are You Sure to Delete?");
            if (input == 0)
            {
                databaseConnection.s.executeUpdate(query);
                btnSave.setText("Save");
                clear();
                loadStudents();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

