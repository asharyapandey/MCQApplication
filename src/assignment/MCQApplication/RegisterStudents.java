    public ArrayList<Student> studentList()
    {
        //loads student data in a array list with objects of Student class
        ArrayList<Student> studentList = new ArrayList<>();
        try
        {
            DatabaseConnection databaseConnection = new DatabaseConnection();
            String query = "select * from student";
            ResultSet rs = databaseConnection.s.executeQuery(query);
            Student student;
            while(rs.next())
            {
                student = new Student(rs.getInt("student_id"), rs.getString("student_name"), rs.getString("batch"), rs.getString("student_username"), rs.getString("student_password"));
                studentList.add(student);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return studentList;
    }
    public void loadStudents()
    {
        ArrayList<Student> studentList = studentList();
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0);
        Object[] row = new Object[5];
        for (int i = 0; i < studentList.size(); i++)
        {
            row[0] = studentList.get(i).getID();
            row[1] = studentList.get(i).getStudentName();
            row[2] = studentList.get(i).getBatch();
            model.addRow(row);
        }
    }
